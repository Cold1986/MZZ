
function openclass(type){
	$("."+type).fadeIn(1000);
	}
function closeclass(type){
	$("."+type).fadeOut(1000);
	}
function openid(type){
	$("#"+type).fadeIn(1000);
	}
function closeid(type){
	$("#"+type).fadeOut(1000);
	}
function showid(type){
	$("#"+type).show();
	}
function hideid(type){
	$("#"+type).hide();
	}
